// ========== 写回阶段特点说明 ==========
// 1. 主要包含组合逻辑，数据选择通路属于组合逻辑
// 2. 不包含流水线寄存器，写回阶段通常不引入额外延迟
// 3. 实现写入时机: 在下一个时钟上升沿写入寄存器组
// 4. 关键路径: wb_mux选择器的输出决定写回寄存器的数据

// ========== 写回阶段的数据流路径 ==========
// 输入(来自MEM阶段) -> wb_mux的选择 -> 输出到regfile
// 所有信号直接传递，不进行修改

endmodule
