// 描述: 流水线的最后一个阶段负责将结果写回寄存器组
// 注: 写回阶段主要包含组合逻辑，不包含流水线寄存器
// ================================================
`timescale 1ns/1ps  // 时间单位: 1纳秒，精度: 1皮秒

// ============================================================================
// 模块: wb_top
// 功能: 写回阶段顶层模块
// 描述:
//   本模块是流水线的最后一个阶段，负责将结果写回寄存器组。
//   它实现了wb_mux选择写回数据，然后将数据、写使能和目标寄存器地址
//   直接传递给寄存器组的写端口。
//
//   特点:
//   - 主要包含组合逻辑，数据选择通路没有时序逻辑
//   - 不包含流水线寄存器，输出直接连接到regfile
//   - 实际寄存器写入发生在下一个时钟周期中
//   - 包含一个性能计数器，统计写回操作的次数
// ============================================================================
module wb_top (
    // ========== 时钟和复位端口 ==========
    input  wire        clk_i,          // 时钟信号 (用于性能计数器)
    input  wire        rst_n_i,        // 复位信号 (低电平有效)

    // ========== 来自MEM/WB寄存器数据输入 ==========
    input  wire [31:0] alu_result_i,   // ALU运算结果
    input  wire [31:0] mem_rdata_i,    // 内存读取数据
    input  wire [31:0] pc_plus4_i,     // PC+4值
    input  wire [31:0] csr_result_i,   // CSR结果

    // ========== 来自MEM/WB寄存器的控制信号 ==========
    input  wire [1:0]  wb_sel_i,       // 写回选择信号
    input  wire        reg_we_i,       // 寄存器写使能
    input  wire [4:0]  rd_addr_i,      // 目标寄存器地址

    // ========== 连接到寄存器组 ==========
    output wire [31:0] wb_data_o,      // 写回数据
    output wire        reg_we_o,       // 寄存器写使能
    output wire [4:0]  rd_addr_o       // 目标寄存器地址
);

// ========== 内部信号定义 ==========
wire [31:0] wb_data;      // 来自wb_mux模块的写回数据
// 该信号是写回数据选择器的输出结果

// ========== 实例化写回数据选择器 ==========
wb_mux u_wb_mux (
    // 输入端口
    .alu_result_i (alu_result_i),  // 输入端口: ALU运算结果
    .mem_rdata_i  (mem_rdata_i),   // 输入端口: 内存读取数据
    .pc_plus4_i   (pc_plus4_i),    // 输入端口: PC+4值
    .csr_data_i   (csr_result_i),         // CSR结果（暂时接地，预留功能）
    // CSR控制和状态寄存器功能将在后续版本中启用

    // 输入端口
    .wb_sel_i     (wb_sel_i),      // 输入端口: 写回选择信号

    // 输出
    .wb_data_o    (wb_data)        // 输出到内部信号wb_data
    // 该信号是经过选择后的写回数据
);

// ========== 输出信号连接 ==========
assign wb_data_o = wb_data;      // 将内部信号连接到输出端口
// 该信号将送到寄存器组的wdata_i端口

assign reg_we_o = reg_we_i;      // 写使能信号直通
// 写回阶段不修改写使能信号，直接传递

assign rd_addr_o = rd_addr_i;    // 目标寄存器地址直通
// 写回阶段不修改目标地址，直接传递


