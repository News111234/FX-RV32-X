# ============================================================
# 测试分支惩罚 (Branch Misprediction Penalty)
# 
# 功能: 执行 N 次循环，每次循环内包含一个 "beq x0, x0, target"
#       该分支总是跳转（实际 taken），而静态预测为 not taken，
#       因此每次循环都会引发一次分支误预测，导致 2~3 个周期的惩罚。
# 
# 通过读取 mcycle 计数器的差值计算总周期数。
# ============================================================

# 寄存器分配
# x10 : 循环计数器 (从 N 递减到 0)
# x11 : 循环次数 N
# x12 : 存储开始时的 mcycle 低32位
# x13 : 存储结束时的 mcycle 低32位
# x14 : 存储差值

# 定义循环次数 (可根据需要修改)
.equ N, 100

.section .text
.globl _start

_start:
    # ---- 初始化 ----
    li   x11, N                # x11 = 循环次数
    csrr x12, mcycle           # 读取开始时的 mcycle 低32位

    # ---- 测试循环 ----
loop:
    beq  x0, x0, target        # 无条件分支，总是跳转 (误预测)
    addi x11, x11, -1 
    addi x10, x10, 1 
target:
    addi x10, x10, 1           # 空操作，仅用于占位
    addi x11, x11, -1          # 循环计数减1


    # ---- 测量结束 ----
    csrr x13, mcycle           # 读取结束时的 mcycle 低32位
    sub  x14, x13, x12         # x14 = 总周期数

    # ---- 将结果写入内存或 GPIO（可根据 SoC 地址修改）----
    # 假设 GPIO 输出寄存器地址为 0x10001000
    li   x15, 0x10001000
    sw   x14, 0(x15)           # 将周期数写入 GPIO 观察

    # ---- 死循环结束 ----
end:
    j end

# ============================================================
# 预期结果分析
# 
# 理想情况（无分支惩罚）: 循环内只有 3 条指令（beq + 2 addi）
# 但实际:
#   - 每次 beq 误预测会引入 2~3 个周期的惩罚（取指、译码刷新）
#   - 所以总周期数 ≈ N * (3 + penalty) + 少量固定开销
# 
# 若 penalty = 2，则 N=100 时总周期约为 500，明显大于 300。
# 可以通过对比关闭分支预测（或使用 always-taken 优化）来验证。
# ============================================================