// rtl/ifu/inst_rom.v - SPI中断测试程序
`timescale 1ns/1ps

module inst_rom (
    input  wire [31:0] addr_i,
    output reg  [31:0] data_o
);

reg [31:0] rom [0:511];
integer i;

initial begin
    // 初始化所有指令为nop
    for (i = 0; i <= 511; i = i + 1) begin
        rom[i] = 32'h00000013; // nop: addi x0, x0, 0
    end

// ============================================================================
// SPI 中断测试程序
// ============================================================================
// 功能说明:
//大概在8.9us中断信号触发，两个周期的中断延迟
//   1. 设置栈指针 (x2 = 0x1000)
//   2. 设置中断向量表 (mtvec = 0x200)
//   3. 使能SPI中断 (mie[12] = 1)
//   4. 全局中断使能 (mstatus.MIE = 1)
//   5. 初始化SPI主机 (时钟分频=100, 使能SPI+中断)
//   6. 发送数据0x55并启动传输
//   7. 主循环等待中断服务程序设置标志
//   8. 中断服务程序: 保存现场, 读取接收数据, 设置标志, 恢复现场, 返回
//   9. 主程序读取接收数据并保存到0x3000, 然后无限循环
// ============================================================================
// SPI寄存器地址映射:
//   REG_CTRL     = 0x10003000  (控制寄存器)
//   REG_CLK_DIV  = 0x10003004  (时钟分频寄存器)
//   REG_DATA     = 0x10003008  (数据寄存器)
//   REG_STATUS   = 0x1000300C  (状态寄存器)
//   REG_IRQ_FLAG = 0x10003010  (中断标志寄存器)
// ============================================================================
// CSR寄存器地址映射:
//   MTVEC    = 0x305 (中断向量表基址)
//   MIE      = 0x304 (中断使能寄存器)
//   MSTATUS  = 0x300 (机器状态寄存器)
//   MCAUSE   = 0x342 (中断/异常原因寄存器)
// ============================================================================

// ----------------------------------------------------------------------------
// 主程序 (地址 0x00 - 0x7C)
// ----------------------------------------------------------------------------

// 第1部分: 设置栈指针 (x2 = 0x1000)
// ============================================================================
rom[0] = 32'h00001137;  // lui   x2, 0x00001000
                         // 将立即数0x1左移12位得到0x1000，存入x2
                         // x2 = 0x1000 (栈顶指针)

rom[1] = 32'h00010113;  // addi  x2, x2, 0x000
                         // x2 = x2 + 0 = 0x1000

// 第2部分: 设置中断向量表 (mtvec = 0x200)
// ============================================================================
rom[2] = 32'h20000293;  // addi  x5, x0, 0x200 (x5 = 0x200)

rom[3] = 32'h30529073;  // csrrw x0, 0x305, x5
                         // 将x5的值写入CSR寄存器mtvec(地址0x305)
                         // mtvec = x5 (中断入口地址)

// 第3部分: 使能SPI中断 (mie[12] = 1)
// ============================================================================
rom[4] = 32'h00001337;  // lui   x6, 0x000010000 (x6 = 0x1000)
                         


rom[5] = 32'h30432073;  // csrrs x0, 0x304, x6
                         // 将x6的值写回MIE寄存器
                         // mie = x6

// 第4部分: 全局中断使能 (mstatus.MIE = 1)
// ============================================================================
rom[8] = 32'h300023f3;  // csrrs x7, 0x300, x0
                         // 读取MSTATUS寄存器(地址0x300)的值到x7
                         // x7 = mstatus

rom[9] = 32'h0083e393;  // ori   x7, x7, 0x008
                         // x7 = x7 | 0x08 (设置bit3)
                         // bit3是MIE (全局中断使能位)

rom[10] = 32'h30039073; // csrrw x0, 0x300, x7
                         // 将x7的值写回MSTATUS寄存器
                         // mstatus = x7

// 第5部分: 初始化SPI主机
// ============================================================================
rom[11] = 32'h10003537; // lui   x10, 0x10003000
                         // 将立即数0x10003左移12位得到0x10003000，存入x10
                         // x10 = SPI基地址

rom[12] = 32'h00050513; // addi  x10, x10, 0x000
                         // x10 = x10 + 0 = 0x10003000

rom[13] = 32'h06400593; // addi  x11, x0, 0x064
                         // x11 = 0 + 100 = 100
                         // 时钟分频值 = 100 → SPI时钟 = 200MHz/(2*100)=1MHz

rom[14] = 32'h00b52223; // sw    x11, 0x004(x10)
                         // 将x11的值写入地址 x10+4 = 0x10003004
                         // 写REG_CLK_DIV，设置SPI时钟速度

rom[15] = 32'h00300593; // addi  x11, x0, 0x003
                         // x11 = 0 + 3 = 3
                         // 二进制: 0b0011
                         // bit0=1 使能SPI
                         // bit1=1 使能SPI中断

rom[16] = 32'h00b52023; // sw    x11, 0x000(x10)
                         // 将x11的值写入地址 x10+0 = 0x10003000
                         // 写REG_CTRL，使能SPI和中断

rom[17] = 32'h00000013; // nop
                         // 等待SPI模块稳定

rom[18] = 32'h00000013; // nop
                         // 等待SPI模块稳定

// 第6部分: 准备数据并启动传输
// ============================================================================
rom[19] = 32'h05500613; // addi  x12, x0, 0x055
                         // x12 = 0 + 85 = 0x55
                         // 要发送的数据

rom[20] = 32'h00c52423; // sw    x12, 0x008(x10)
                         // 将x12的值写入地址 x10+8 = 0x10003008
                         // 写REG_DATA，数据进入发送缓冲区

rom[21] = 32'h04300593; // addi  x11, x0, 0x043
                         // x11 = 0 + 67 = 0x43
                         // 二进制: 0b0100_0011
                         // bit0=1 保持SPI使能
                         // bit1=1 保持中断使能
                         // bit6=1 启动一次传输

rom[22] = 32'h00b52023; // sw    x11, 0x000(x10)
                         // 将x11的值写入地址 x10+0 = 0x10003000
                         // 写REG_CTRL，bit6=1触发start_tx脉冲
                         // SPI开始发送0x55

// 第7部分: 主循环 (等待中断服务程序设置标志)
// ============================================================================
rom[23] = 32'h00402783; // lw    x15, 0x0004(x0)
                         // 读取内存地址0x2000的值到x15
                         // 这个地址由中断服务程序写入标志

rom[24] = 32'hfe078ee3; // beq   x15, x0, main_loop
                         // 如果x15 == 0，跳转到main_loop (地址0x5C)
                         // 继续等待；如果x15 != 0，顺序执行

rom[25] = 32'h00000013; // nop
                         // 分支延迟槽

rom[26] = 32'h00000013; // nop
                         // 分支后停顿

// 第8部分: 读取接收数据并保存
// ============================================================================
rom[27] = 32'h00002803; // lw    x16, 0x0000(x0)
                         // 读取内存地址0x2004的值到x16
                         // 这个地址保存了SPI接收到的数据

rom[28] = 32'h01002023; // sw    x16, 0x0008(x0)
                         // 将x16的值存入内存地址0x3000
                         // 保存最终结果

// 第9部分: 测试结束，无限循环
// ============================================================================
rom[29] = 32'h0000006f; // jal   x0, end_loop
                         // 无条件跳转到自身，形成无限循环
                         // 程序停在这里

rom[30] = 32'h00000013; // nop
                         // 分支延迟槽

rom[31] = 32'h00000013; // nop
                         // 分支后停顿

// ----------------------------------------------------------------------------
// 中断服务程序 (入口地址: 0x200)
// ----------------------------------------------------------------------------

// 第10部分: 保存现场 (保存被调用者保存的寄存器)
// ============================================================================
rom[128] = 32'hfc010113; // addi  sp, sp, -64
                          // 栈指针下移64字节，分配栈空间

rom[129] = 32'h00112023; // sw    x1, 0(sp)
                          // 保存返回地址寄存器ra (x1)

rom[130] = 32'h00a12223; // sw    x10, 4(sp)
                          // 保存x10

rom[131] = 32'h00b12423; // sw    x11, 8(sp)
                          // 保存x11

rom[132] = 32'h00c12623; // sw    x12, 12(sp)
                          // 保存x12

rom[133] = 32'h00d12823; // sw    x13, 16(sp)
                          // 保存x13

rom[134] = 32'h00e12a23; // sw    x14, 20(sp)
                          // 保存x14

// 第11部分: 读取中断原因
// ============================================================================
rom[135] = 32'h34202573; // csrrs x10, 0x342, x0
                          // 读取MCAUSE寄存器(地址0x342)的值到x10
                          // x10 = mcause
                          // mcause最高位=1表示中断，低12位是中断ID

// 第12部分: 判断是否是中断 (最高位为1)
// ============================================================================
rom[136] = 32'h00054c63; // blt   x10, x0, check_spi
                          // 如果x10 < 0 (即最高位为1)，跳转到check_spi
                          // 因为中断的最高位是1，所以会跳转

rom[137] = 32'h00000013; // nop
                          // 分支延迟槽

rom[138] = 32'h00000013; // nop
                          // 分支后停顿

rom[139] = 32'h0400006f; // jal   x0, isr_exit
                          // 无条件跳转到isr_exit (地址0x29C)
                          // 如果不是中断，直接退出

rom[140] = 32'h00000013; // nop
                          // 分支延迟槽

rom[141] = 32'h00000013; // nop
                          // 分支后停顿

// 第13部分: 检查是否是SPI中断 (ID=12)
// ============================================================================
rom[142] = 32'h7ff57593; // andi  x11, x10, 0x7ff
                          // x11 = x10 & 0x7FF
                          // 提取mcause的低11位 (中断ID)

rom[143] = 32'h00c00613; // addi  x12, x0, 0x00c
                          // x12 = 0 + 12 = 12
                          // SPI中断ID = 12

rom[144] = 32'h02c59663; // bne   x11, x12, isr_exit
                          // 如果x11 != 12，跳转到isr_exit
                          // 不是SPI中断则退出

rom[145] = 32'h00000013; // nop
                          // 分支延迟槽

rom[146] = 32'h00000013; // nop
                          // 分支后停顿

// 第14部分: SPI中断服务程序
// ============================================================================
rom[147] = 32'h100036b7; // lui   x13, 0x10003000
                          // x13 = SPI基地址 (0x10003000)

rom[148] = 32'h00068693; // addi  x13, x13, 0x000
                          // x13 = 0x10003000

rom[149] = 32'h00300713; // addi  x14, x0, 0x003
                          // x14 = 3 (二进制0b0011)
                          // bit0=1 清除发送中断标志
                          // bit1=1 清除接收中断标志

rom[150] = 32'h00e6a823; // sw    x14, 0x010(x13)
                          // 将x14写入地址 x13+16 = 0x10003010
                          // 写REG_IRQ_FLAG，清除中断标志

rom[151] = 32'h0086a703; // lw    x14, 0x008(x13)
                          // 读取地址 x13+8 = 0x10003008
                          // 读REG_DATA，获取接收到的数据
                          // x14 = 从机发来的数据

rom[152] = 32'h00e02023; // sw    x14, 0x0000(x0)
                          // 将x14存入内存地址0x0000
                          // 保存接收到的数据

rom[153] = 32'h00100793; // addi  x15, x0, 0x001
                          // x15 = 1

rom[154] = 32'h00f02223; // sw    x15, 0x0004(x0)
                          // 将x15存入内存地址0x0004
                          // 设置标志位，告诉主程序传输完成

// 第15部分: 恢复现场并返回
// ============================================================================
rom[155] = 32'h00012083; // lw    x1, 0(sp)
                          // 恢复返回地址ra (x1)

rom[156] = 32'h00412503; // lw    x10, 4(sp)
                          // 恢复x10

rom[157] = 32'h00812583; // lw    x11, 8(sp)
                          // 恢复x11

rom[158] = 32'h00c12603; // lw    x12, 12(sp)
                          // 恢复x12

rom[159] = 32'h01012683; // lw    x13, 16(sp)
                          // 恢复x13

rom[160] = 32'h01412703; // lw    x14, 20(sp)
                          // 恢复x14

rom[161] = 32'h04010113; // addi  sp, sp, 64
                          // 栈指针上移64字节，释放栈空间

rom[162] = 32'h30200073; // mret
                          // 机器模式异常返回
                          // 恢复PC到mepc，恢复mstatus

end

always @(*) begin
    if (addr_i[31:2] <= 511) begin
        data_o = rom[addr_i[31:2]];
    end else begin
        data_o = 32'h00000013; // nop
    end
end

endmodule