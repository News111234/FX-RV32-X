// rtl/ifu/inst_rom_hello.v - SPI测试程序
`timescale 1ns/1ps

module inst_rom_hello (
    input  wire [31:0] addr_i,
    output reg  [31:0] data_o
);

reg [31:0] rom [0:511];
integer i;

initial begin
    // 初始化所有指令为nop
    for (i = 0; i <= 511; i = i + 1) begin
        rom[i] = 32'h00000013; // nop: addi x0, x0, 0
    end

// ============================================================================
// SPI 主机模式测试程序 - 完整注释版
// ============================================================================
// SPI寄存器地址映射:
//   REG_CTRL     = 0x10003000  (控制寄存器)
//   REG_CLK_DIV  = 0x10003004  (时钟分频寄存器)
//   REG_DATA     = 0x10003008  (数据寄存器)
//   REG_STATUS   = 0x1000300C  (状态寄存器)
//   REG_IRQ_FLAG = 0x10003010  (中断标志寄存器)
// ============================================================================

// ---------- 初始化阶段：设置SPI基地址 ----------
rom[0] = 32'h10003537;  // lui x10, 0x10003000
                         // 将立即数0x10003左移12位得到0x10003000，存入x10
                         // x10现在等于SPI外设的基地址

rom[1] = 32'h00050513;  // addi x10, x10, 0x000
                         // x10 = x10 + 0，保持地址不变
                         // 现在x10 = 0x10003000

// ---------- 配置SPI时钟分频 ----------
rom[2] = 32'h000005b7;  // lui x11, 0x00000000
                         // x11高20位清零

rom[3] = 32'h06458593;  // addi x11, x11, 0x064
                         // x11 = x11 + 100 = 100
                         // 时钟分频值 = 100
                         // 系统时钟200MHz / 100 = 2MHz SPI时钟

rom[4] = 32'h00b52223;  // sw x11, 0x004(x10)
                         // 将x11的值(100)写入地址 x10+4 = 0x10003004
                         // 这个地址是REG_CLK_DIV，设置SPI时钟速度

// ---------- 使能SPI主机 ----------
rom[5] = 32'h00100593;  // addi x11, x0, 0x001
                         // x11 = 0 + 1 = 1
                         // 二进制: 0b0000_0001
                         // bit0=1 表示使能SPI
                         // bit6=0 表示不启动传输

rom[6] = 32'h00b52023;  // sw x11, 0x000(x10)
                         // 将x11的值(1)写入地址 x10+0 = 0x10003000
                         // 这个地址是REG_CTRL，使能SPI
                         // 执行后SPI模块的spi_enable信号变为1

rom[7] = 32'h00000013;  // nop
                         // 空操作，等待SPI模块稳定

rom[8] = 32'h00000013;  // nop
                         // 空操作，等待SPI模块稳定

// ---------- 第1次传输：准备发送数据0x55 ----------
rom[9] = 32'h05500613;  // addi x12, x0, 0x055
                         // x12 = 0 + 85 = 0x55
                         // 这是要发送给从机的数据

rom[10] = 32'h00c52423; // sw x12, 0x008(x10)
                         // 将x12的值(0x55)写入地址 x10+8 = 0x10003008
                         // 这个地址是REG_DATA，数据进入发送缓冲区

// ---------- 启动SPI传输 ----------
rom[11] = 32'h04100593; // addi x11, x0, 0x041
                         // x11 = 0 + 65 = 0x41
                         // 二进制: 0b0100_0001
                         // bit0=1 保持SPI使能
                         // bit6=1 启动一次传输

rom[12] = 32'h00b52023; // sw x11, 0x000(x10)
                         // 将x11的值(0x41)写入地址 x10+0 = 0x10003000
                         // 写入REG_CTRL，bit6=1触发start_tx脉冲
                         // SPI开始发送0x55，同时接收从机数据

rom[13] = 32'h00000013; // nop
                         // 空操作，等待传输开始

rom[14] = 32'h00000013; // nop
                         // 空操作，等待传输开始

// ---------- 等待传输完成 ----------
rom[15] = 32'h00c52683; // lw x13, 0x00C(x10)    wait_55   0x3c,第一个跳转点
                         // 读取地址 x10+12 = 0x1000300C (REG_STATUS)
                         // x13 = 状态寄存器的值

rom[16] = 32'h0046f693; // andi x13, x13, 0x004
                         // x13 = x13 & 4，提取bit2 (busy位)
                         // 如果busy=1，则x13=4；如果busy=0，则x13=0

rom[17] = 32'h00000013; // nop
                         // 空操作，分支前停顿

rom[18] = 32'h00000013; // nop
                         // 空操作，分支前停顿

rom[19] = 32'hfe0698e3; // bne x13, x0, wait_55  0x4c
                         // 如果x13不等于0（即busy=1），跳转到地址0x3C(rom[15])
                         // 继续等待；如果busy=0，顺序执行下一指令

rom[20] = 32'h00000013; // nop
                         // 分支延迟槽，不执行有用操作

rom[21] = 32'h00000013; // nop
                         // 分支后停顿

// ---------- 读取接收到的数据 ----------
rom[22] = 32'h00852703; // lw x14, 0x008(x10)
                         // 读取地址 x10+8 = 0x10003008 (REG_DATA)
                         // x14 = 从机发来的数据

rom[23] = 32'h00e02023; // sw x14, 0x000(x0)
                         // 将x14的值存入内存地址 0x0000
                         // 保存第一次传输接收到的数据

rom[24] = 32'h00000013; // nop
                         // 空操作

rom[25] = 32'h00000013; // nop
                         // 空操作

// ---------- 第2次传输：发送数据0xAA ----------
rom[26] = 32'h0aa00613; // addi x12, x0, 0x0AA
                         // x12 = 170 (0xAA)

rom[27] = 32'h00c52423; // sw x12, 0x008(x10)
                         // 写REG_DATA，准备发送0xAA

rom[28] = 32'h04100593; // addi x11, x0, 0x041
                         // x11 = 0x41 (bit0=1使能, bit6=1启动)

rom[29] = 32'h00b52023; // sw x11, 0x000(x10)
                         // 写REG_CTRL，启动第二次传输

rom[30] = 32'h00000013; // nop
rom[31] = 32'h00000013; // nop

rom[32] = 32'h00c52683; // lw x13, 0x00C(x10)
                         // 读REG_STATUS，检查busy位

rom[33] = 32'h0046f693; // andi x13, x13, 0x004
                         // 提取busy位

rom[34] = 32'h00000013; // nop
rom[35] = 32'h00000013; // nop

rom[36] = 32'hfe0698e3; // bne x13, x0, wait_aa
                         // busy=1则等待

rom[37] = 32'h00000013; // nop
rom[38] = 32'h00000013; // nop

rom[39] = 32'h00852703; // lw x14, 0x008(x10)
                         // 读REG_DATA，获取接收数据

rom[40] = 32'h00e02223; // sw x14, 0x004(x0)
                         // 存入内存地址0x0004

rom[41] = 32'h00000013; // nop
rom[42] = 32'h00000013; // nop

// ---------- 第3次传输：发送数据0x12 ----------
rom[43] = 32'h01200613; // addi x12, x0, 0x012
                         // x12 = 18 (0x12)

rom[44] = 32'h00c52423; // sw x12, 0x008(x10)

rom[45] = 32'h04100593; // addi x11, x0, 0x041

rom[46] = 32'h00b52023; // sw x11, 0x000(x10)

rom[47] = 32'h00000013; // nop
rom[48] = 32'h00000013; // nop

rom[49] = 32'h00c52683; // lw x13, 0x00C(x10)
rom[50] = 32'h0046f693; // andi x13, x13, 0x004
rom[51] = 32'h00000013; // nop
rom[52] = 32'h00000013; // nop

rom[53] = 32'hfe0698e3; // bne x13, x0, wait_12

rom[54] = 32'h00000013; // nop
rom[55] = 32'h00000013; // nop

rom[56] = 32'h00852703; // lw x14, 0x008(x10)
rom[57] = 32'h00e02423; // sw x14, 0x008(x0)

rom[58] = 32'h00000013; // nop
rom[59] = 32'h00000013; // nop

// ---------- 第4次传输：发送数据0x34 ----------
rom[60] = 32'h03400613; // addi x12, x0, 0x034
                         // x12 = 52 (0x34)

rom[61] = 32'h00c52423; // sw x12, 0x008(x10)

rom[62] = 32'h04100593; // addi x11, x0, 0x041

rom[63] = 32'h00b52023; // sw x11, 0x000(x10)

rom[64] = 32'h00000013; // nop
rom[65] = 32'h00000013; // nop

rom[66] = 32'h00c52683; // lw x13, 0x00C(x10)
rom[67] = 32'h0046f693; // andi x13, x13, 0x004
rom[68] = 32'h00000013; // nop
rom[69] = 32'h00000013; // nop

rom[70] = 32'hfe0698e3; // bne x13, x0, wait_34

rom[71] = 32'h00000013; // nop
rom[72] = 32'h00000013; // nop

rom[73] = 32'h00852703; // lw x14, 0x008(x10)
rom[74] = 32'h00e02623; // sw x14, 0x00C(x0)

rom[75] = 32'h00000013; // nop
rom[76] = 32'h00000013; // nop

// ---------- 无限循环，测试结束 ----------
rom[77] = 32'h0000006f; // jal x0, end_loop
                         // 无条件跳转到自身，形成无限循环
                         // 程序停在这里

rom[78] = 32'h00000013; // nop
                         // 分支延迟槽

rom[79] = 32'h00000013; // nop
                         // 分支后停顿

end

always @(*) begin
    if (addr_i[31:2] <= 511) begin
        data_o = rom[addr_i[31:2]];
    end else begin
        data_o = 32'h00000013; // nop
    end
end

endmodule