// inst_rom_uart_basic.v - UART基础功能测试
// 功能: 测试UART发送使能、START命令、轮询发送

`timescale 1ns/1ps

module inst_rom_hello (
    input  wire [31:0] addr_i,
    output reg  [31:0] data_o
);

reg [31:0] rom [0:511];
integer i;

initial begin
    // 初始化所有指令为nop
    for (i = 0; i <= 511; i = i + 1) begin
        rom[i] = 32'h00000013; // nop: addi x0, x0, 0
    end

// ============================================================================
// UART寄存器地址
// UART_BASE = 0x10000000
//   TX_DATA  = 0x00
//   RX_DATA  = 0x04
//   STATUS   = 0x08
//   CTRL     = 0x0C
//   BAUD_DIV = 0x10
// ============================================================================

// ============================================================================
// 主程序入口 (地址 0x00)
// ============================================================================

// 初始化栈指针
rom[0]   = 32'h10001137;  // lui   sp, 0x10001000     (sp = 0x10001000)

// ========== 1. 初始化UART ==========
rom[1]   = 32'h100002b7;  // lui   t0, 0x10000000      (t0 = UART_BASE)
rom[2]   = 32'h00028293;  // addi  t0, t0, 0        (t0 = 0x10000000)

// 使能发送器和接收器 (tx_enable=1, rx_enable=1)
rom[3]   = 32'h00300313;  // addi  t1, x0, 3        (t1 = 0x03)
rom[4]   = 32'h0062a623;  // sw    t1, 12(t0)       (CTRL = 0x03)

// ========== 2. 写入字符串到FIFO ==========
// "Hello UART!\n\r" 共13个字符

// 'H' (0x48)
rom[5]   = 32'h04800313;  // addi  t1, x0, 0x48
rom[6]   = 32'h0062a023;  // sw    t1, 0(t0)        (TX_DATA = 'H')

// 'e' (0x65)
rom[7]   = 32'h06500313;  // addi  t1, x0, 0x65
rom[8]   = 32'h0062a023;  // sw    t1, 0(t0)

// 'l' (0x6C)
rom[9]   = 32'h06c00313;  // addi  t1, x0, 0x6C
rom[10]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'l' (0x6C)
rom[11]  = 32'h06c00313;  // addi  t1, x0, 0x6C
rom[12]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'o' (0x6F)
rom[13]  = 32'h06f00313;  // addi  t1, x0, 0x6F
rom[14]  = 32'h0062a023;  // sw    t1, 0(t0)

// ' ' (0x20)
rom[15]  = 32'h02000313;  // addi  t1, x0, 0x20
rom[16]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'U' (0x55)
rom[17]  = 32'h05500313;  // addi  t1, x0, 0x55
rom[18]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'A' (0x41)
rom[19]  = 32'h04100313;  // addi  t1, x0, 0x41
rom[20]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'R' (0x52)
rom[21]  = 32'h05200313;  // addi  t1, x0, 0x52
rom[22]  = 32'h0062a023;  // sw    t1, 0(t0)

// 'T' (0x54)
rom[23]  = 32'h05400313;  // addi  t1, x0, 0x54
rom[24]  = 32'h0062a023;  // sw    t1, 0(t0)

// '!' (0x21)
rom[25]  = 32'h02100313;  // addi  t1, x0, 0x21
rom[26]  = 32'h0062a023;  // sw    t1, 0(t0)

// '\n' (0x0A)
rom[27]  = 32'h00a00313;  // addi  t1, x0, 0x0A
rom[28]  = 32'h0062a023;  // sw    t1, 0(t0)

// '\r' (0x0D)
rom[29]  = 32'h00d00313;  // addi  t1, x0, 0x0D
rom[30]  = 32'h0062a023;  // sw    t1, 0(t0)

// ========== 3. 启动发送 ==========
rom[31]  = 32'h00c2a283;  // lw    t0, 12(t0)        (读取CTRL)
rom[32]  = 32'h04028293;  // addi  t0, t0, 0x40      (设置START_TX位)
rom[33]  = 32'h0052a223;  // sw    t0, 12(t0)        (写回CTRL)

// ========== 4. 等待发送完成 ==========
// 读取STATUS，检查TX_DONE位 (bit7)
rom[34]  = 32'h0082a283;  // lw    t0, 8(t0)         (读取STATUS)
rom[35]  = 32'h0802f293;  // andi  t0, t0, 0x80      (提取bit7)
rom[36]  = 32'hfe0286e3;  // beq   t0, x0, -40       (跳到rom[34]，等待完成)
                          // JAL后需要2个NOP
rom[37]  = 32'h00000013;  // nop
rom[38]  = 32'h00000013;  // nop

// ========== 5. 清除完成标志 ==========
rom[39]  = 32'h100002b7;  // lui   t0, 0x10000000
rom[40]  = 32'h00028293;  // addi  t0, t0, 0
rom[41]  = 32'h00c2a283;  // lw    t0, 12(t0)
rom[42]  = 32'h01028293;  // addi  t0, t0, 0x10      (设置CLR_TX_IRQ位)
rom[43]  = 32'h100002b7;  // lui   t0, 0x10000000
rom[44]  = 32'h00028293;  // addi  t0, t0, 0
rom[45]  = 32'h0052a223;  // sw    t0, 12(t0)

// ========== 6. 无限循环 ==========
rom[46]  = 32'h0000006f;  // jal   x0, 0x00          (跳转到自身)
                          // JAL后需要2个NOP
rom[47]  = 32'h00000013;  // nop
rom[48]  = 32'h00000013;  // nop

end

always @(*) begin
    if (addr_i[31:2] <= 511) begin
        data_o = rom[addr_i[31:2]];
    end else begin
        data_o = 32'h00000013; // nop
    end
end

endmodule