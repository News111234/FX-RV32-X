// rtl/ifu/inst_rom_hello.v - I2C测试程序
`timescale 1ns/1ps

module inst_rom (
    input  wire [31:0] addr_i,
    output reg  [31:0] data_o
);

reg [31:0] rom [0:511];
integer i;

initial begin
    // 初始化所有指令为nop
    for (i = 0; i <= 511; i = i + 1) begin
        rom[i] = 32'h00000013; // nop: addi x0, x0, 0
    end

// ============================================================================
// I2C 中断测试程序 还有一些是指令编排的问题，导致并不能完全按照注释的要求实现，只需编排指令即可实现
//I2C的多阶段传输，还有中断都成功实现了,总流程大约391us
//START:0-20US,SEND_ADDR:20-180US,REC_ACK:180-200US,SEND_DATA:200-360US,REC_ACK:360-380US,
//STOP:380--390US,interrupt:390.5us触发，然后进入到中断服务程序，最后在主程序中读取结果并保存到内存
// ============================================================================
// 功能说明:
//   1. 设置栈指针 (x2 = 0x1000)
//   2. 设置中断向量表 (mtvec = 0x200)
//   3. 使能外部中断 (mie[11] = 1) - I2C中断合并到外部中断
//   4. 全局中断使能 (mstatus.MIE = 1)
//   5. 初始化I2C主机 (时钟分频=2000, 使能I2C+中断)
//   6. 向地址0x50的从机发送数据0xA5
//   7. 主循环等待中断服务程序设置标志
//   8. 中断服务程序: 保存现场, 读取状态, 处理中断, 恢复现场, 返回
//   9. 主程序读取结果并保存到内存, 然后无限循环
// ============================================================================
// I2C寄存器地址映射:
//   I2C_BASE    = 0x10004000
//   I2C_CTRL    = 0x10004000 (偏移0x00)
//   I2C_CLK_DIV = 0x10004004 (偏移0x04)
//   I2C_TX_DATA = 0x10004008 (偏移0x08)
//   I2C_RX_DATA = 0x1000400C (偏移0x0C)
//   I2C_STATUS  = 0x10004010 (偏移0x10)
//   I2C_ADDR    = 0x10004014 (偏移0x14)
//   I2C_IRQ_FLAG= 0x10004018 (偏移0x18)
// ============================================================================
// CSR寄存器地址映射:
//   MTVEC    = 0x305 (中断向量表基址)
//   MIE      = 0x304 (中断使能寄存器)
//   MSTATUS  = 0x300 (机器状态寄存器)
//   MCAUSE   = 0x342 (中断/异常原因寄存器)
// ============================================================================

// ----------------------------------------------------------------------------
// 主程序 (地址 0x00 - 0x7C)
// ----------------------------------------------------------------------------

// 第1部分: 设置栈指针 (x2 = 0x1000)
// ============================================================================
rom[0] = 32'h00001137;  // lui   x2, 0x00001000 (x2 = 0x1000)
rom[1] = 32'h00010113;  // addi  x2, x2, 0

// 第2部分: 设置中断向量表 (mtvec = 0x200)
// ============================================================================
rom[2] = 32'h20000293;  // addi  x5, x0, 0x200 (x5 = 0x200)
rom[3] = 32'h30529073;  // csrrw x0, mtvec, x5

// 第3部分: 使能外部中断 (mie[11] = 1)
// ============================================================================
rom[4] = 32'h000015b7;  // lui x11, 0x00001000 (x11 = 0x1000)
rom[5] = 32'h80058593;  // addi x11, x11, -2048 (x11 = 0x1000 - 0x800 = 0x800)
rom[6] = 32'h30459073;  // csrrw x0, mie, x11 (mie[11] = 1)

// 第4部分: 全局中断使能 (mstatus.MIE = 1)
// ============================================================================
rom[7] = 32'h300023f3;  // csrrs x7, mstatus, x0
rom[8] = 32'h0083e393;  // ori   x7, x7, 0x008
rom[9] = 32'h30039073;  // csrrw x0, mstatus, x7

// 第5部分: 初始化I2C主机
// ============================================================================
rom[10]  = 32'h10004537; // lui   x10, 0x10004000 (x10 = 0x10004000)
rom[11] = 32'h00050513; // addi  x10, x10, 0

// 设置时钟分频 (200MHz / 2000 = 100kHz)
rom[12] = 32'h7d000593; // addi  x11, x0, 2000 (0x7D0)
rom[13] = 32'h00b52223; // sw    x11, 0x04(x10)  // I2C_CLK_DIV

// 使能I2C + 使能中断 (0x03)
rom[14] = 32'h00300593; // addi  x11, x0, 0x03 (bit0=1使能, bit1=1中断)
rom[15] = 32'h00b52023; // sw    x11, 0x00(x10)  // I2C_CTRL

rom[16] = 32'h00000013; // nop
rom[17] = 32'h00000013; // nop

// 第6部分: 设置从设备地址 (0x50)
// ============================================================================
rom[18] = 32'h05000593; // addi  x11, x0, 0x50 (从机地址)
rom[19] = 32'h00b52a23; // sw    x11, 0x14(x10)  // I2C_ADDR

// 第7部分: 准备数据并启动写传输
// ============================================================================
rom[20] = 32'h0a500613; // addi  x12, x0, 0xA5 (要发送的数据)
rom[21] = 32'h00c52423; // sw    x12, 0x08(x10)  // I2C_TX_DATA

// 启动写传输 (CTRL: bit0=1使能, bit2=1启动, bit4=0写)
rom[22] = 32'h00500593; // addi  x11, x0, 0x05 (二进制0101)
rom[23] = 32'h00b52023; // sw    x11, 0x00(x10)  // I2C_CTRL

// 第8部分: 主循环 (等待中断服务程序设置标志)
// ============================================================================
rom[24] = 32'h00402783; // lw    x15, 0x0004(x0)  // 等待标志位
rom[25] = 32'hfe078ee3; // beq   x15, x0, -36     // 跳到rom[23]
rom[26] = 32'h00000013; // nop
rom[27] = 32'h00000013; // nop

// 第9部分: 读取结果并保存
// ============================================================================
// 注意: 写操作没有接收数据，只检查状态
// 这里读取状态寄存器的ACK位来判断传输是否成功
rom[28] = 32'h01002583; // lw    x11, 0x10(x0)   // 读I2C_STATUS
rom[29] = 32'h0085f593; // andi  x11, x11, 0x08  // 提取ACK位(bit3)
rom[30] = 32'h00100513; // addi  x10, x0, 0x01   // 默认成功标志
rom[31] = 32'h00058863; // beq   x11, x0, 0x0c   // 如果ACK=0则成功 跳转到i2c_success
rom[32] = 32'h00000013; // nop
rom[33] = 32'h00000013; // nop
rom[34] = 32'h00000513; // addi  x10, x0, 0x00   // NACK则失败标志=0
rom[35] = 32'h00a02223; // sw    x10, 0x0008(x0) // 存结果到mem[2]  success

// 第10部分: 测试结束，无限循环
// ============================================================================
rom[36] = 32'h0000006f; // jal   x0, 0x8C         // 自跳转
rom[37] = 32'h00000013; // nop
rom[38] = 32'h00000013; // nop

// ----------------------------------------------------------------------------
// 中断服务程序 (入口地址: 0x200)
// ----------------------------------------------------------------------------

// 第11部分: 保存现场
// ============================================================================
rom[128] = 32'hfc010113; // addi  sp, sp, -64   sp:x2
rom[129] = 32'h00112023; // sw    ra, 0(sp)
rom[130] = 32'h00a12223; // sw    x10, 4(sp)
rom[131] = 32'h00b12423; // sw    x11, 8(sp)
rom[132] = 32'h00c12623; // sw    x12, 12(sp)
rom[133] = 32'h00d12823; // sw    x13, 16(sp)
rom[134] = 32'h00e12a23; // sw    x14, 20(sp)

// 第12部分: 读取中断原因 (mcause)
// ============================================================================
rom[135] = 32'h34202573; // csrrs x10, mcause, x0

// 第13部分: 判断是否是中断 (最高位为1)
// ============================================================================
rom[136] = 32'h00054c63; // blt   x10, x0, 24     // 跳到check_intr    00054c63
rom[137] = 32'h00000013; // nop
rom[138] = 32'h00000013; // nop
rom[139] = 32'h0600006f; // jal   x0, 96         // 跳到isr_exit
rom[140] = 32'h00000013; // nop
rom[141] = 32'h00000013; // nop
// 第14部分: 检查是否是外部中断 (ID=11)
// ============================================================================
rom[142] = 32'h7ff57593; // andi  x11, x10, 0x7ff   check_intr
rom[143] = 32'h00b00613; // addi  x12, x0, 11
rom[144] = 32'h04c59663; // bne   x11, x12, 76    // 不是外部中断则退出
rom[145] = 32'h00000013; // nop
rom[146] = 32'h00000013; // nop

// 第15部分: I2C中断处理
// ============================================================================
rom[147] = 32'h100046b7; // lui   x13, 0x10004000 (I2C基地址)
rom[148] = 32'h00068693; // addi  x13, x13, 0

// 读取I2C状态寄存器，判断中断类型
rom[149] = 32'h0106a783; // lw    x15, 0x10(x13)  // 读I2C_STATUS

// 清除中断标志 (写IRQ_FLAG寄存器)
rom[150] = 32'h00300713; // addi  x14, x0, 0x03   // 清除TX和RX中断标志
rom[151] = 32'h00e6ac23; // sw    x14, 0x18(x13)  // 写I2C_IRQ_FLAG

// 检查是否有NACK错误
rom[152] = 32'h0087f793; // andi  x15, x15, 0x08  // 提取ACK位
rom[153] = 32'h02078063; // beq   x15, x0, 32     跳转到i2c_success // 如果没有NACK则成功 
rom[154] = 32'h00000013; // nop
rom[155] = 32'h00000013; // nop

// NACK错误处理
rom[156] = 32'h00000793; // addi  x15, x0, 0x00   // 失败标志=0
rom[157] = 32'h00f02223; // sw    x15, 0x0004(x0) // 存标志位  
rom[158] = 32'h0100006f; // jal   x0, 16           // 跳到恢复现场,set_flag

rom[159] = 32'h00000013; // nop
rom[160] = 32'h00000013; // nop

// 传输成功
rom[161] = 32'h00100793; // addi  x15, x0, 0x01   // 成功标志=1  i2c_success
rom[162] = 32'h00f02223; // sw    x15, 0x0004(x0) // 存标志位  set_flag

// 第16部分: 恢复现场并返回
// ============================================================================
rom[163] = 32'h00012083; // lw    ra, 0(sp)   //isr_exit
rom[164] = 32'h00412503; // lw    x10, 4(sp)
rom[165] = 32'h00812583; // lw    x11, 8(sp)
rom[166] = 32'h00c12603; // lw    x12, 12(sp)
rom[167] = 32'h01012683; // lw    x13, 16(sp)
rom[168] = 32'h01412703; // lw    x14, 20(sp)
rom[169] = 32'h04010113; // addi  sp, sp, 64
rom[170] = 32'h30200073; // mret

end

always @(*) begin
    if (addr_i[31:2] <= 511) begin
        data_o = rom[addr_i[31:2]];
    end else begin
        data_o = 32'h00000013; // nop
    end
end

endmodule

